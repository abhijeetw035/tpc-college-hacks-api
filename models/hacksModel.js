const mongoose = require("mongoose");

const hackSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Hack = mongoose.model("Hack", hackSchema);
module.exports = Hack;
